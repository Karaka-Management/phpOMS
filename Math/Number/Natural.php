class Natural
{
    /**
     * Is natural number.
     *
     * @param mixed $value Value to test
     *
     * @return bool
     *
     * @since  1.0.0
     * @author Dennis Eichhorn <d.eichhorn@oms.com>
     */
    public static function isNatural($value) : bool
    {
        return is_int($value) && $value >= 0;
    }
}